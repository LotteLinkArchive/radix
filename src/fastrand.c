uint64_t fr_genkeystream(
    void *dest,
    register uint64_t seed,
    register uint32_t numBytes)
{
    register uint8_t seed_m0, seed_m1;
    register uint8_t *dcstream = dest;
    
    while (numBytes--)
    {
        seed_m0 = seed ^ 0x6D;
        seed_m1 = seed ^ 0xBB;
        *dcstream++ = (uint8_t)(fr_lookup0[seed_m0] ^ fr_lookup1[seed_m1]);

        seed++;
    }
    
    return seed;
}

uint64_t fr_64noise(uint64_t seed)
{
    uint64_t fragments;
    (void)fr_genkeystream(&fragments, seed, 8);
    return fragments;
}

uint8_t fr_8noise(uint8_t seed)
{
    uint8_t fragments;
    (void)fr_genkeystream(&fragments, seed, 1);
    return fragments;
}

void RadixAbstract_RandomizeBlob(RadixMemoryBlob blob, uint64_t seed)
{
    fr_genkeystream(
        RadixAbstract_GetBlobPointer(blob),
        seed,
        RadixAbstract_GetBlobLength(blob));
}

int fr_uniquerandom(int n) {
    if ((n - 1) == RAND_MAX) {
        return rand();
    }
    
    assert (n <= RAND_MAX);

    int end = RAND_MAX / n;
    assert (end > 0);
    end *= n;

    int r;
    while ((r = rand()) >= end);

    return r % n;
}
